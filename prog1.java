// Find the Sum of Array Elements

public class prog1 {
    public static void main(String[] args){
        int[] arr = {1, 2, 3, 4, 5};

        // using for loop
        int sum = 0;
        for(int num: arr){
            sum += num;
        }

        System.out.println("Sum of Array Elements :- ");
        System.out.println(sum);
    }
}


